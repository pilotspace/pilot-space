const PROVIDER_ICON_COLORS: Record<string, string> = {
  google: 'text-foreground',
  anthropic: 'text-[#D4845C]',
  ollama: 'text-foreground',
  openai: 'text-foreground',
  custom: 'text-muted-foreground',
};

const PROVIDER_ICON_COMPONENTS: Record<
  string,
  React.ComponentType<{ className?: string; size?: number }>
> = {
  google: GoogleIcon,
  anthropic: AnthropicIcon,
  ollama: OllamaIcon,
  openai: OpenAIIcon,
};

export function ProviderIcon({ provider, className, size = 16 }: ProviderIconProps) {
  const IconComponent = PROVIDER_ICON_COMPONENTS[provider];
  const colorClass = PROVIDER_ICON_COLORS[provider] ?? 'text-muted-foreground';

  if (IconComponent) {
    return <IconComponent className={cn(colorClass, className)} size={size} />;
  }

  return (
    <Cpu className={cn('text-muted-foreground', className)} style={{ width: size, height: size }} />
  );
}
